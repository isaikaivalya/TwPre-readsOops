public class DerivedOops {
    private  int WIDTH;
    private  int HEIGHT;
    private  int DIAMETER;

    public DerivedOops(int WIDTH,int HEIGHT, int DIAMETER) {
        this.WIDTH=WIDTH;
        this.HEIGHT=HEIGHT;
        this.DIAMETER=DIAMETER;
    }
    public DerivedOops(){

    }
    public void setWIDTH()
    {
        this.WIDTH=WIDTH;
    }
    public void setHEIGHT()
    {
        this.HEIGHT=HEIGHT;
    }
    public void setDIAMETER()
    {
        this.DIAMETER=DIAMETER;
    }
    public int getWIDTH()
    {
        return WIDTH;
    }
    public int getHEIGHT()
    {
        return HEIGHT;
    }
    public int getDIAMETER()
    {
        return DIAMETER;
    }
}
